"""
FeedbackModule: Handles logging and evaluation for continuous improvement of llm, knowledge base, and fusion.
"""

from src.utils.logger import Logger

class FeedbackModule:
    @staticmethod
    def log_feedback(request, responses):
        """
        Log the feedback and evaluation metrics for future improvements.
        
        Args:
            request (dict): The original input request.
            responses (dict): Responses generated by the expert bots.
        """
        logger = Logger()
        # For now, simply log the feedback; later, this can be extended to integrate with
        # monitoring or automated improvement systems.
        logger.log_activity(f"Feedback - Request: {request}\nResponses: {responses}") 